ASSUME CS:CODE, DS:DATA

DATA SEGMENT
	NUM1 DB 15H, 17H
	NUM2 DB 23H, 9H
	NUM3 DB 00H, 00H
DATA ENDS

CODE SEGMENT
START:
	MOV AX, DATA
	MOV DS, AX

	XOR AX, AX

	MOV AL, NUM1
	MOV BL, NUM2
	ADD AL, BL
	DAA
	MOV NUM3, AL

	XOR AL, AL
	XOR BL, BL

	MOV AL, NUM1+1
	MOV BL, NUM2+1
	ADD AL, BL
	DAA
	MOV NUM3+1, AL

	INT 03H
CODE ENDS
END START