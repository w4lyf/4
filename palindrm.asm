ASSUME DS:DATA, CS:CODE 

DATA SEGMENT
	MSG DB "RADAR $"
	MSG1 DB "WORD IS A PALINDROME $"
 	MSG2 DB "NOT A PALINDROME $"
	COUNT DW 05H
DATA ENDS

CODE SEGMENT
	DISP PROC NEAR
		MOV AH, 09H
		INT 21H
		RET
	DISP ENDP

START: 
	MOV AX,DATA
	MOV DS,AX
       	MOV CX,COUNT 
       	LEA SI,MSG 
       	LEA DI,MSG 
       	ADD DI,CX 
       	DEC DI 

REPEAT: 
	MOV AL,[SI]
       	CMP AL,[DI]
       	JNZ NOT_PALINDROME
       	INC SI
       	DEC DI
	DEC CX
	JNZ REPEAT

	LEA DX,MSG1
	CALL DISP
	JMP FINISH

NOT_PALINDROME: 
	LEA DX,MSG2
       	CALL DISP

FINISH: 
	MOV AX,4C00H
      	INT 21H
      	INT 03H
CODE ENDS
END START
