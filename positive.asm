ASSUME DS:DATA, CS:CODE

DATA SEGMENT
	NUM DB -01H
	RESULT DB ?
DATA ENDS

CODE SEGMENT
START:  
	MOV AX, DATA
        MOV DS, AX

	XOR AX, AX

        MOV AH, NUM
        RCL AH, 1
	JC IS_NEGATIVE
	MOV RESULT, 0
	JMP FINISH

IS_NEGATIVE:
	MOV RESULT, 1

FINISH:
        INT 03
CODE ENDS
END START
