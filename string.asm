ASSUME DS:DATA, CS:CODE

DATA SEGMENT
        MSG1 DB "HELLO $"
        MSG2 DB 0AH, 'HI $'
DATA ENDS  
  	
CODE SEGMENT
	DISP PROC NEAR
		MOV AH,09H
		INT 21H
		RET
	DISP ENDP

START:  
	MOV  AX, DATA
        MOV  DS, AX
        
	LEA  DX,MSG1
        CALL DISP
        
	LEA  DX,MSG2 ;
        CALL DISP
	
	MOV AX,4C00H
	INT 21H
	INT 03H
CODE ENDS
END START   
